def area(a, b):
    """
    Calculates the area of rectangle

    Params:
    a (int) - width
    b (int) - heigth

    Returns area of rectangle with width a and heigth b
    """
    return a * b


def perimeter(a, b):
    """
    Calculates the perimeter of rectangle

    Params:
    a (int) - width
    b (int) - heigth

    Returns perimeter of rectangle with width a and heigth b
    """
    return 2 * (a + b)
