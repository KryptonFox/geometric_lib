def area(a, h):
    """
    Calculates the area of triangle

    Params:
    a (int) - base
    h (int) - heigth

    Returns area of triangle with width a base and h heigth
    """
    return a * h / 2


def perimeter(a, b, c):
    """
    Calculates the perimeter of triangle

    Params:
    a (int) - 1st side
    b (int) - 2nd side
    c (int) - 3rd sides

    Returns perimeter of triangle with a, b, c sides
    """
    return a + b + c
