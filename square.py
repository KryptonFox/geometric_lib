def area(a):
    """
    Calculates the area of square

    Params:
    a (int) - side

    Returns area of square with side a
    """
    return a * a


def perimeter(a):
    """
    Calculates the perimeter of square

    Params:
    a (int) - side

    Returns perimeter of square with side a
    """
    return 4 * a
